#include <stdio.h>
int main()
 {
    int n,i,j;
    char ch;

    printf("ENTER THE NUMBER OF ROWS\n");
    scanf("%d",&n);

    for(ch='A';i<='A'+i;i++)
     {
        for(j=1;j<=i;j++)
        {
            printf("%c", ch);
        }
        printf("\n");
    }

    return 0;
}